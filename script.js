//While

var x = 0;
while (x < 10) {
    console.log(x);
    document.write("<p>O valor de x é: " + x + "</p>");

    x++;
}

document.write("<p>=============================================================================================================================================================</p>");
//For

for (var y = 0; y <= 10; y++) {
    console.log(y);
    document.write("<p>O valor de y é: " + y + "</p>");
}